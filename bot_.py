@bot.message_handler(commands=['mystats'])
def mystats(message):
    cursor.execute("SELECT COUNT(*) FROM files WHERE user_id=?", (message.from_user.id,))
    count = cursor.fetchone()[0]
    bot.send_message(message.chat.id, f"You uploaded {count} files")



@bot.message_handler(commands=['search'])
def search(message):
    keyword = message.text.replace("/search", "").strip().lower()

    cursor.execute("""
    SELECT file_name, tag FROM files
    WHERE (file_name LIKE ? OR tag LIKE ?) AND user_id = ?
    """, (f"%{keyword}%", f"%{keyword}%", message.from_user.id))

    results = cursor.fetchall()

    if results:
        text = "Results:\n"
        for r in results[:10]:
            text += f"{r[0]} ({r[1]})\n"
        bot.send_message(message.chat.id, text)
    else:
        bot.send_message(message.chat.id, " No results found")


# 📄 DOCUMENT
@bot.message_handler(content_types=['document'])
def handle_document(message):
    try:
        caption = message.caption or "general"
        tag = caption.split()[0].lower()

        file_info = bot.get_file(message.document.file_id)
        downloaded = bot.download_file(file_info.file_path)

        file_name = message.document.file_name
        file_path = f"downloads/documents/{file_name}"

        with open(file_path, 'wb') as f:
            f.write(downloaded)

        save_file(message.from_user.id, file_name, "document",
                  message.document.file_size, tag)

        bot.reply_to(message, f"📄 Saved: {file_name} ({tag})")

    except Exception as e:
        logging.error(e)

