import telebot
import logging
import time
import os
import sqlite3
from datetime import datetime
from openpyxl import Workbook

TOKEN = "TOKEN"
ADMIN_ID = 8369307958

bot = telebot.TeleBot(TOKEN)
logging.basicConfig(level=logging.INFO)

os.makedirs("downloads/audio", exist_ok=True)
os.makedirs("downloads/documents", exist_ok=True)
os.makedirs("downloads/images", exist_ok=True)

conn = sqlite3.connect("bot.db", check_same_thread=False)
cursor = conn.cursor()

cursor.execute("""
CREATE TABLE IF NOT EXISTS users (
    user_id INTEGER PRIMARY KEY,
    name TEXT
)
""")

cursor.execute("""
CREATE TABLE IF NOT EXISTS files (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    user_id INTEGER,
    file_name TEXT,
    file_type TEXT,
    file_size INTEGER,
    tag TEXT,
    date TEXT
)
""")

conn.commit()


def is_admin(user_id):
    return user_id == ADMIN_ID

def save_user(user_id, name):
    cursor.execute("INSERT OR IGNORE INTO users VALUES (?, ?)", (user_id, name))
    conn.commit()

def save_file(user_id, file_name, file_type, file_size, tag):
    cursor.execute("""
    INSERT INTO files (user_id, file_name, file_type, file_size, tag, date)
    VALUES (?, ?, ?, ?, ?, ?)
    """, (user_id, file_name, file_type, file_size, tag,
          datetime.now().strftime("%Y-%m-%d %H:%M")))
    conn.commit()

