@bot.message_handler(commands=['delete'])
def delete_file(message):
    try:
        target = message.text.replace("/delete", "").strip()

        if not target:
            return bot.reply_to(message, "⚠️ Usage: /delete filename")

        cursor.execute("""
        SELECT file_type FROM files
        WHERE file_name=? AND user_id=?
        """, (target, message.from_user.id))

        result = cursor.fetchone()

        if result:
            file_type = result[0]

            folder_map = {
                "document": "documents",
                "audio": "audio",
                "image": "images"
            }

            folder = folder_map.get(file_type, "documents")
            path = f"downloads/{folder}/{target}"

            if os.path.exists(path):
                os.remove(path)

            cursor.execute("""
            DELETE FROM files
            WHERE file_name=? AND user_id=?
            """, (target, message.from_user.id))
            conn.commit()

            bot.reply_to(message, f"🗑️ Deleted: {target}")
        else:
            bot.reply_to(message, "❌ File not found")

    except Exception as e:
        logging.error(e)

